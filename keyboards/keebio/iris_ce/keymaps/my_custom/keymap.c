#include QMK_KEYBOARD_H

enum custom_layers {
    _QWERTY,
    _LOWER,
    _RAISE
};

enum custom_keycodes {
    ALT_H = SAFE_RANGE,
    ALT_J,
    ALT_K,
    ALT_L
};
// Define the RGB color change function
void set_layer_rgb(uint8_t layer) {
    switch (layer) {
        case _LOWER:
            rgblight_enable_noeeprom();  // Enable RGB
            rgblight_sethsv_noeeprom(HSV_RED); // Change to red color
            break;
        case _RAISE:
            rgblight_enable_noeeprom();  // Enable RGB
            rgblight_sethsv_noeeprom(HSV_BLUE); // Change to blue color
            break;
        default: // For _QWERTY and _ADJUST layers
            rgblight_disable_noeeprom(); // Turn off RGB
            break;
    }
}

